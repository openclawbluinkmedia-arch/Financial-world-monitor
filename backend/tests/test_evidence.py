@pytest.mark.asyncio
async def test_log_deduplication(mock_db):
    dedup = DeduplicationService(mock_db)

    await dedup.log_deduplication(
        uuid.uuid4(),
        uuid.uuid4(),
        "exact",
        1.0
    )

    mock_db.add.assert_called_once()
    added_log = mock_db.add.call_args[0][0]
    assert isinstance(added_log, EvidenceDedupLog)
    assert added_log.dedup_type == "exact"
    assert added_log.similarity_score == 1.0


