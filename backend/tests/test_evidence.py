@pytest.mark.asyncio
async def test_deduplication_exact_duplicate(mock_db, sample_item):
    dedup = DeduplicationService(mock_db)

    existing = Evidence(
        id=uuid.uuid4(),
        evidence_id="existing-id",
        source_id=sample_item.source_id,
        source_name="RBI",
        content_hash=sample_item.content_hash,
        near_dup_hash=sample_item.near_dup_hash,
    )

    mock_result = MagicMock()
    mock_result.scalar_one_or_none.return_value = existing
    mock_db.execute.return_value = mock_result

    result = await dedup.check_exact_duplicate(sample_item.content_hash)
    assert result is existing


